function toggleEditPanel() {
  const panel = document.getElementById('edit-panel');
  const btn   = document.getElementById('edit-toggle-btn');
  const open  = panel.classList.toggle('open');
  btn.textContent = '';
  btn.innerHTML = open
    ? `<svg viewBox="0 0 14 14" style="width:13px;height:13px;stroke:var(--navy);stroke-width:2;fill:none"><path d="M10 2l2 2-7 7H3V9l7-7z"/><path d="M8.5 3.5l2 2"/></svg> Close Editor`
    : `<svg viewBox="0 0 14 14" style="width:13px;height:13px;stroke:var(--navy);stroke-width:2;fill:none"><path d="M10 2l2 2-7 7H3V9l7-7z"/><path d="M8.5 3.5l2 2"/></svg> Edit Table`;
  if (open) renderEditPanel();
}

function applyChanges() {
  // Title / subtitle
  CONFIG.title    = document.getElementById('ep-title').value.trim();
  CONFIG.subtitle = document.getElementById('ep-sub').value.trim();

  // Columns
  document.querySelectorAll('.ep-col').forEach(inp => {
    CONFIG.columns[+inp.dataset.i] = inp.value.trim();
  });

  // Rows labels
  document.querySelectorAll('.ep-row-label').forEach(inp => {
    CONFIG.rows[+inp.dataset.idx].label = inp.value.trim();
  });

  // Row values
  document.querySelectorAll('.ep-row-val').forEach(inp => {
    CONFIG.rows[+inp.dataset.idx].values[+inp.dataset.vi] = inp.value.trim();
  });

  renderTable();
  renderEditPanel();
}

function removeRow(idx) {
  CONFIG.rows.splice(idx, 1);
  renderTable();
  renderEditPanel();
}

function addRow() {
  CONFIG.rows.push({
    label: "New factor",
    values: CONFIG.columns.map(()=>"—")
  });
  renderTable();
  renderEditPanel();
  // Scroll to bottom of rows list
  const rows = document.getElementById('ep-rows');
  if (rows) rows.lastElementChild?.scrollIntoView({behavior:'smooth',block:'nearest'});
}

