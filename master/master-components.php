function renderTable() {
  // Header
  document.getElementById('hdr-title').textContent = CONFIG.title;
  document.getElementById('hdr-sub').textContent   = CONFIG.subtitle;

  const cols = CONFIG.columns.length + 1; // +1 for Factor label
  const colTpl = `280px ${Array(CONFIG.columns.length).fill('1fr').join(' ')}`;

  const wrap = document.getElementById('tbl-wrap');
  wrap.innerHTML = '';

  // Header row
  const headRow = mkEl('div', 'tbl-row tbl-row--head');
  headRow.style.gridTemplateColumns = colTpl;
  headRow.setAttribute('role', 'row');

  const factorTh = mkEl('div', 'td');
  factorTh.setAttribute('role', 'columnheader');
  factorTh.textContent = 'Factor';
  headRow.appendChild(factorTh);

  CONFIG.columns.forEach((col, i) => {
    const th = mkEl('div', 'td');
    th.setAttribute('role', 'columnheader');
    th.textContent = col;
    headRow.appendChild(th);
  });
  wrap.appendChild(headRow);

  // Body rows
  const body = mkEl('div', '');
  body.id = 'tbl-body';
  body.setAttribute('role', 'rowgroup');

  CONFIG.rows.forEach((row, idx) => {
    const tr = mkEl('div', `tbl-row ${idx % 2 === 0 ? 'tbl-row--odd' : 'tbl-row--even'}${idx >= CONFIG.visibleRows ? ' tbl-row--hidden' : ''}`);
    tr.style.gridTemplateColumns = colTpl;
    tr.setAttribute('role', 'row');

    const label = mkEl('div', 'td td--label');
    label.setAttribute('role', 'cell');
    label.textContent = row.label;
    tr.appendChild(label);

    row.values.forEach((val, vi) => {
      const td = mkEl('div', vi === 0 ? 'td td--vc' : 'td td--val');
      td.setAttribute('role', 'cell');
      td.textContent = val;
      tr.appendChild(td);
    });

    body.appendChild(tr);
  });

  wrap.appendChild(body);

  // Update view more button visibility
  const btn = document.getElementById('view-btn');
  if (CONFIG.rows.length <= CONFIG.visibleRows) {
    btn.style.display = 'none';
  } else {
    btn.style.display = 'inline-flex';
    // Reset state
    document.getElementById('tbl-body').classList.remove('show-all');
    btn.classList.remove('open');
    document.getElementById('btn-lbl').textContent = 'View More';
    btn.setAttribute('aria-expanded', 'false');
  }
}

