        // === Form Validation ===
        function showError(elementId, message) {
            const element = document.getElementById(elementId);
            if (element) {
                element.textContent = message || 'This field is required';
                element.classList.remove('hidden');
                
                if (elementId.includes('Error') && !elementId.includes('scope')) {
                    const inputId = elementId.replace('Error', '');
                    const input = document.getElementById(inputId);
                    if (input) input.classList.add('error');
                }
            }
        }

        function hideError(elementId) {
            const element = document.getElementById(elementId);
            if (element) {
                element.classList.add('hidden');
                
                if (elementId.includes('Error') && !elementId.includes('scope')) {
                    const inputId = elementId.replace('Error', '');
                    const input = document.getElementById(inputId);
                    if (input) input.classList.remove('error');
                }
            }
        }

        function validateEmail(email) {
            return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
        }

        // === Form Submission ===
        async function handleSubmit(e) {
            e.preventDefault();
            
            // Clear all errors
            ['scopeError', 'fullNameError', 'emailError'].forEach(hideError);
            
            // Validate scope
            if (selectedScopes.size === 0) {
                showError('scopeError', 'Select at least one scope');
                return;
            }
            
            // Get form data
            const formData = {
                fullName: document.getElementById('fullName').value.trim(),
                email: document.getElementById('email').value.trim(),
                company: document.getElementById('company').value.trim(),
                phone: document.getElementById('phone').value.trim(),
                message: document.getElementById('message').value.trim(),
                scopes: Array.from(selectedScopes)
            };
            
            // Validate required fields
            let hasError = false;
            
            if (!formData.fullName) {
                showError('fullNameError', 'Name is required');
                hasError = true;
            }
            
            if (!formData.email) {
                showError('emailError', 'Email is required');
                hasError = true;
            } else if (!validateEmail(formData.email)) {
                showError('emailError', 'Invalid email format');
                hasError = true;
            }
            
            if (hasError) return;
            
            // Show loading state
            const btn = document.getElementById('submitBtn');
            const btnText = document.getElementById('btnText');
            const btnArrow = document.getElementById('btnArrow');
            const btnSpinner = document.getElementById('btnSpinner');
            
            btn.disabled = true;
            btnText.textContent = 'Sending...';
            btnArrow.classList.add('hidden');
            btnSpinner.classList.remove('hidden');
            
            // Simulate API call
            await new Promise(resolve => setTimeout(resolve, 2000));
            
            // Show success
            document.getElementById('successName').textContent = formData.fullName;
            document.getElementById('formView').classList.add('hidden');
            document.getElementById('successView').classList.remove('hidden');
            
            // Reset button
            btn.disabled = false;
            btnText.textContent = 'Send Inquiry';
            btnArrow.classList.remove('hidden');
            btnSpinner.classList.add('hidden');
            
            console.log('Form submitted:', formData);
        }

