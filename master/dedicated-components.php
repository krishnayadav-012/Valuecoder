    el('tcc-team-size').style.setProperty('--pct', ((size - 2)     / (25 - 2)  * 100) + '%');
    el('tcc-duration').style.setProperty ('--pct', ((duration - 1) / (24 - 1) * 100) + '%');
 
    /* VC cost */
    var vcMo    = size * VC_RATES[seniority];
    var vcTotal = vcMo * duration;
 
    /* In-house cost */
    var annualSalary = IH_SALARY[market][seniority];
    var monthSalary  = annualSalary / 12;
    var ihBase       = monthSalary * OVERHEAD;
