  function updateCalc() {
    var size      = +el('tcc-team-size').value;
    var seniority =  el('tcc-seniority').value;
    var market    =  el('tcc-market').value;
    var duration  = +el('tcc-duration').value;
 
    /* Labels */
    el('tcc-size-val').textContent = size + ' engineer' + (size === 1 ? '' : 's');
