      function activateTab(e) {
        e.preventDefault();
        
        // Deactivate all tabs
        tabLabels.forEach(function(label, index){
          label.classList.remove("active");
        });
        [].forEach.call(tabPanes, function(pane, index){
          pane.classList.remove("active");
